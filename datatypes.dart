void main() {
  //Data types: Numbers (Int, double), Strings, Boolean, Lists, Maps, Dynamic.

  //Strings
  String name = "Srinivas";
  print("Value of string: $name");

  //Numbers - Integers
  int num = 69;
  print("Int: $num");

  //Numbers - Double
  double othernum = 6.9;
  print("Other num: $othernum");

  //Booleans
  bool trueBool = true;
  bool falseBool = false;
  print("True: $trueBool, False: $falseBool");

  //Dynamic - if you haven't defined the type of the variable, you can use dynamic
  dynamic dynamicVar = "Dynamic variable";
  print("Dynamic variable: $dynamicVar");
}
