void main() {
  //Using the var keyword:
  var name = "Srinivas";

  //By specifying the data type:
  String name1 = "Srinivas";
  int x = 69;
  print(x);

  //You can use the dynamic keyword to declare variables that can change their data type:
  dynamic name2 = "Srinivas";

  //Constants are declared using the const keyword:
  const double pi = 3.14;
  const String name3 = "Hegde";

  //Declaring but not using it
  String name4;
}
